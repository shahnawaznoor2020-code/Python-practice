#\n - new/next line
#\t - tab
#\\ - backslash
#\' - insert a single quote in a single-quoted line

#\n
print("Hello World.\nHow are you?")

#\t
print("John 20")
print("John\t20")

#\\
#print("new\old")  -error {escape sequce warning|}
